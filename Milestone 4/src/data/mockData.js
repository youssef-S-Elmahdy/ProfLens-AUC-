// Helper functions
export const getProfessorById = (id) => professors.find(p => p.id === id);
export const getCourseById = (id) => courses.find(c => c.id === id);
export const getReviewsByTarget = (type, targetId) =>
  reviews.filter(r => r.type === type && r.targetId === targetId);
export const getProfessorsByCourse = (courseId) => {
  const course = getCourseById(courseId);
  if (!course) return [];
  return course.professors.map(profId => getProfessorById(profId)).filter(Boolean);
};
export const getCoursesByProfessor = (professorId) => {
  const professor = getProfessorById(professorId);
  if (!professor) return [];
  return professor.courses.map(code => courses.find(c => c.code === code)).filter(Boolean);
};

// Search function
export const searchAll = (query) => {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return { professors: [], courses: [] };

  const matchedProfessors = professors.filter(p =>
    p.firstName.toLowerCase().includes(normalizedQuery) ||
    p.lastName.toLowerCase().includes(normalizedQuery) ||
    `${p.firstName} ${p.lastName}`.toLowerCase().includes(normalizedQuery) ||
    p.departmentName.toLowerCase().includes(normalizedQuery)
  );

  const matchedCourses = courses.filter(c =>
    c.code.toLowerCase().includes(normalizedQuery) ||
    c.name.toLowerCase().includes(normalizedQuery) ||
    c.departmentName.toLowerCase().includes(normalizedQuery)
  );

  return { professors: matchedProfessors, courses: matchedCourses };
};
